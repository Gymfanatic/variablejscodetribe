// Data types
let name = "Neo";  // String
let age = 25;      // Number
let isActive = true; // Boolean
let fruits = ["Apple", "Banana", "Orange"]; // Array
let person = { // Object
  firstName: "Neo",
  lastName: "Mabasa",
  age: 25
};

// Operators
let num1 = 10;
let num2 = 5;

// Arithmetic Operators
let sum = num1 + num2; // Addition
let difference = num1 - num2; // Subtraction
let product = num1 * num2; // Multiplication
let quotient = num1 / num2; // Division
let remainder = num1 % num2; // Modulo

console.log("Arithmetic Operators:");
console.log("Sum: " + sum);
console.log("Difference: " + difference);
console.log("Product: " + product);
console.log("Quotient: " + quotient);
console.log("Remainder: " + remainder);

// Comparison Operators
let isEqual = num1 == num2; // Equal to
let isNotEqual = num1 != num2; // Not equal to
let isGreaterThan = num1 > num2; // Greater than
let isLessThan = num1 < num2; // Less than

console.log("\nComparison Operators:");
console.log("Is Equal: " + isEqual);
console.log("Is Not Equal: " + isNotEqual);
console.log("Is Greater Than: " + isGreaterThan);
console.log("Is Less Than: " + isLessThan);

// Logical Operators
let andResult = (num1 > num2) && (num1 > 0); // AND
let orResult = (num1 < num2) || (num1 > 0); // OR
let notResult = !(num1 < num2); // NOT

console.log("\nLogical Operators:");
console.log("AND Result: " + andResult);
console.log("OR Result: " + orResult);
console.log("NOT Result: " + notResult);
